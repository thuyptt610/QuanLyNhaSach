    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        DefaultTableModel  dtm = new DefaultTableModel();

        String[] tieude = {"Mã Nhà xuất bản", "Tên xuất bản","Địa Chỉ","Điện Thoại"};
        dtm.setColumnIdentifiers(tieude);

        if(txtTimKiem.getText().isEmpty())
        HienThi();
        else{
            ArrayList<NHACC> dsncc = DAO_NHACC.timKiemNHACCTheoTen(txtTimKiem.getText());
            dtm.setRowCount(0);
            for(NHACC ncc:dsncc){
                Vector<Object> vec = new Vector<Object>();
                vec.add(ncc.getMaNhacc());
                vec.add(ncc.getTenNhacc());
                vec.add(ncc.getDienThoai());
                vec.add(ncc.getDiaChi());

                dtm.addRow(vec);
            }
            dsNHACC.setModel(dtm);
            txtTimKiem.setText("");
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

