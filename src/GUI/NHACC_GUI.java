public void HienThi(){
        DefaultTableModel  dtm = new DefaultTableModel();
        String[] tieude = {"Mã Nhà xuất bản", "Tên Nhà xuất bản","Địa Chỉ","Điện Thoại"};
        dtm.setColumnIdentifiers(tieude);
        ArrayList<NHACC> dsHH = DAO_NHACC.getNHACC();
        
        for(NHACC hh:dsHH){
            Vector<Object> vec = new Vector<Object>();
                vec.add(hh.getMaNhacc());
                vec.add(hh.getTenNhacc());
                vec.add(hh.getDienThoai());
                vec.add(hh.getDiaChi());
            dtm.addRow(vec);
        }
        dsNHACC.setModel(dtm);
     }
 private void xoaNCC() {
    int row = dsNHACC.getSelectedRow();
    if (row >= 0) {
        String maNCC = dsNHACC.getValueAt(row, 0).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa nhà xuất bản này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                boolean result = DAO_NHACC.xoaNHACC(maNCC);
                if (result) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Không thể xóa nhà xuất bản", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
                HienThi();
            } catch (SQLException ex) {
                Logger.getLogger(NHACC_GUI.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Lỗi khi xóa nhà xuất bản", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
    }
}
